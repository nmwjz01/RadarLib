				break;
			case PALETTE_COLOR_YELLOWBLUERED:
				m_szPalette[i] = palette_YellowBlueRed[i][0] * 256 * 256 + palette_YellowBlueRed[i][1] * 256 + palette_YellowBlueRed[i][2];
