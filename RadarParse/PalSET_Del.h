				break;
			case PALETTE_COLOR_GREENGRAYRED:
				m_szPalette[i] = palette_GreenGrayRed[i][0] * 256 * 256 + palette_GreenGrayRed[i][1] * 256 + palette_GreenGrayRed[i][2];
				break;
