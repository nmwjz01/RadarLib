				break;
			case PALETTE_COLOR_GREENGRAYWHITE1:
				m_szPalette[i] = palette_GreenGrayWhite1[i][0] * 256 * 256 + palette_GreenGrayWhite1[i][1] * 256 + palette_GreenGrayWhite1[i][2];
				break;
			case PALETTE_COLOR_GREENGRAYWHITE2:
				m_szPalette[i] = palette_GreenGrayWhite2[i][0] * 256 * 256 + palette_GreenGrayWhite2[i][1] * 256 + palette_GreenGrayWhite2[i][2];
				break;
			case PALETTE_COLOR_BLUERED1:
				m_szPalette[i] = palette_BlueRed1[i][0] * 256 * 256 + palette_BlueRed1[i][1] * 256 + palette_BlueRed1[i][2];
				break;
			case PALETTE_COLOR_BLUERED2:
				m_szPalette[i] = palette_BlueRed2[i][0] * 256 * 256 + palette_BlueRed2[i][1] * 256 + palette_BlueRed2[i][2];
				break;
			case PALETTE_COLOR_BLUERED3:
				m_szPalette[i] = palette_BlueRed3[i][0] * 256 * 256 + palette_BlueRed3[i][1] * 256 + palette_BlueRed3[i][2];
				break;
			case PALETTE_COLOR_BLUERED4:
				m_szPalette[i] = palette_BlueRed4[i][0] * 256 * 256 + palette_BlueRed4[i][1] * 256 + palette_BlueRed4[i][2];
				break;
