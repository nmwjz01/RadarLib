				m_szPalette[i] = palette_YellowGreenWhite[i][0] * 256 * 256 + palette_YellowGreenWhite[i][1] * 256 + palette_YellowGreenWhite[i][2];
				break;
			case PALETTE_COLOR_BLUEGRAYWHITE:
				m_szPalette[i] = palette_BlueGrayWhite[i][0] * 256 * 256 + palette_BlueGrayWhite[i][1] * 256 + palette_BlueGrayWhite[i][2];
				break;
